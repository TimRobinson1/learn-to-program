# Five squared
puts 5**2

# Square root of Five
puts 5**0.5

# Seven divided by 3, without remainder
puts 7/3

# Using a modulus to give the remainder of the division
puts 7%3

# Ditto, but weeks into a year
puts 365%7


# Using 'abs' finds the absolute value of the number.  Both produce '3'.
puts (5-2).abs
puts (2-5).abs
